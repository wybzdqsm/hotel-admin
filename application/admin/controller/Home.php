<?php

namespace app\admin\controller;

use think\Controller;
use think\Request;
use think\Db;
use think\response\Json;

class Home extends Controller
{
    /**
     * 显示资源列表
     *
     * @return \think\Response
     */

    public $code;
    public $validate;

    public  function __construct(Request $request = null)
    {
        parent::__construct($request);
        $this->code=config('code');
    }

    protected function _initialize()
    {
        parent::_initialize(); // TODO: Change the autogenerated stub
          checkToken();
    }

    public function index()
    {
        //
        $data=$this->request->get();
        $this->validate=validate('Home');

        if(isset($data["page"])  && !empty($data["page"])){
            $page=$data["page"];
        }else{
            $page=1;
        }
        if(isset($data["limit"])  && !empty($data["limit"])){
            $limit=$data["limit"];
        }else{
            $limit=10;
        }
        $where=[];
        if(isset($data['scity']) && !empty($data['scity'])){
            $where['scity']=$data['scity'];
        }
        if(isset($data["sname"])  && !empty($data["sname"])){
            $where["sname"]=['like','%'.$data["sname"].'%'];
        }

        $result=Db::table('home')->where($where)->paginate($limit,false,['page'=>$page]);
        $homedata=$result->items();
        $total=$result->total();
        if($result){
            return json([
                'code'=>200,
                'msg'=>'民宿查询成功',
                'data'=>$homedata,
                'total'=>$total,

            ]);
        }else{
            return json([
                'code'=>404,
                'msg'=>'民宿查询失败'
            ]);
        }
    }

    /**
     * 显示创建资源表单页.
