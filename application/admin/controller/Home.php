    /**
     * 保存新建的资源
     *
     * @param  \think\Request  $request
     * @return \think\Response
     */
    public function save(Request $request)
    {
        //

        $data=$this->request->post();
         $data['ctime']=time();
        $result=Db::table('home')->insert($data);
        if($result){
            return json([
                'code'=>200,
                'msg'=>'民宿添加成功'
            ]);
        }else{
            return json([
                'code'=>404,
                'msg'=>'民宿添加失败'
            ]);
        }

    }

    /**
     * 显示指定的资源
     *
     * @param  int  $id
     * @return \think\Response
     */
    public function read($id)
    {
        //

       $result= Db::table('home')->where('sid',$id)->find();
        $result['sbanner1']=explode(',',$result['sbanner']);
        if($result){

            return json([
                'code'=>200,
                'msg'=>'数据读取成功',
                'data'=>$result
            ]);
        }else{
            return json([
                'code'=>404,
                'msg'=>'暂无数据',

            ]);
        }
    }

