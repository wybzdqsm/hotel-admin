    /**
     * 保存更新的资源
     *
     * @param  \think\Request  $request
     * @param  int  $id
     * @return \think\Response
     */
    public function update(Request $request, $id)
    {
        $data=$request->put();
        $result=Db::table('home')->where('sid',$id)->update($data);
        if($result){
            return json([
                'code'=>200,
                'msg'=>'数据更新成功',

            ]);
        }else{
            return json([
                'code'=>404,
                'msg'=>'数据更新失败'
            ]);
        }
        //

    }

    /**
     * 删除指定资源
     *
     * @param  int  $id
     * @return \think\Response
     */
    public function delete($id)
    {
        //

        $result=Db::table('home')->where('sid',$id)->delete();
        if($result){
            return json([
                'code'=>200,
                'msg'=>'删除成功',
            ]);
        }else{
            return json([
                'code'=>404,
                'msg'=>'删除失败'
            ]);
        }


    }
